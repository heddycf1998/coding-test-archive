function accum(s) {
    let pisah = s.split('')
    let hasil = []
    // console.log(pisah)
    for(let i = 0; i < pisah.length; i++) {
        let pertama = pisah[i].toUpperCase()
        let kedua = pisah[i].toLowerCase().repeat(i)
        hasil.push(pertama + kedua) 
    }
    return hasil.join('-')
}




